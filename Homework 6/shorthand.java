class shorthand {
    public static void main(String[] args) {
       int var = 100;
       
       var += 5;
       System.out.println(var);
       
       var -= 100;
       System.out.println(var);
       
       var *= 500;
       System.out.println(var);
       
       var /= 2;
       System.out.println(var);
    }
}